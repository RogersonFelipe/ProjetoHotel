    //Montar Objeto
    private Hospedes createObject(){
        Hospedes h = new Hospedes();
        String email = FTEmail.getText().trim();
        if(TCodigo.getText().isEmpty()){
            h.setId(0);
        }else{
            h.setId(Integer.parseInt(TCodigo.getText()));
        }
        if (!email.isEmpty() && !email.matches("^[\\w._%+-]+@[\\w.-]+\\.[a-zA-Z]{2,6}$")) {
            JOptionPane.showMessageDialog(this, "E-mail inv√°lido!", "Erro", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        h.setNome(TNome.getText());
        h.setEmail(email);
        h.setTelefone(FTTelefone.getText());
        
        return h;
        
    }
    
