    private void BtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSalvarActionPerformed
        Hospedes h = this.createObject();
        try{
            if(TCodigo.getText().isEmpty()){
                dao.create(h);
                JOptionPane.showMessageDialog(null, "Registro Criado com Sucesso");
            }else{
                dao.update(h);
                JOptionPane.showMessageDialog(null, "Registro Atualizado com Sucesso");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Erro ao Gravar" + ex.getMessage());
        }
        this.limparComponentes();
        this.loadTable();
    }//GEN-LAST:event_BtnSalvarActionPerformed

