    // Montar Objeto
    private Quartos createObject(){
        Quartos q = new Quartos();
        
        if(TCodigo.getText().isEmpty()){
            q.setId(0);
        } else {
            q.setId(Integer.parseInt(TCodigo.getText()));
        }
        
        q.setNumero_quarto(TNumeroQuarto.getText());
        q.setTipo_quarto((TipoQuarto) CTipoQuarto.getSelectedItem());
        q.setStatus((Status) CStatus.getSelectedItem());
        Object valor = FTValorDiaria.getValue();
        BigDecimal preco;
        if (valor == null) {
            preco = BigDecimal.ZERO; 
        } else {
            try {
                preco = BigDecimal.valueOf(((Number) valor).doubleValue());
            } catch (Exception e) {
                preco = BigDecimal.ZERO;
            }
        }
        q.setPreco_diaria(preco);
        return q;
    }
    
    //Popular Componentes
    private void loadComponents(Quartos q){
        TCodigo.setText(q.getId()+"");
        TNumeroQuarto.setText(q.getNumero_quarto());
        CTipoQuarto.setSelectedItem(q.getTipo_quarto());
        CStatus.setSelectedItem(q.getStatus());
        FTValorDiaria.setValue(q.getPreco_diaria());
    }
    
