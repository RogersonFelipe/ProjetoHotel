    private void BtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSalvarActionPerformed
        Quartos q = this.createObject();
        String numero = TNumeroQuarto.getText().trim();
        if (numero.length() > 4) {
            JOptionPane.showMessageDialog(this,
                    "O número do quarto deve ter no máximo 4 caracteres (ex: AB24).",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE);
            TNumeroQuarto.requestFocus();
            return;
        }
        if (numero.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "O número do quarto não pode estar vazio.",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE);
            TNumeroQuarto.requestFocus();
            return;
        }
        
        
        try{
            if(TCodigo.getText().isEmpty()){
                dao.create(q);
                JOptionPane.showMessageDialog(null, "Registro Criado com Sucesso");
            }else{
                dao.update(q);
                JOptionPane.showMessageDialog(null, "Registro Atualizado com Sucesso");
            }
        } catch(Exception ex){
            JOptionPane.showMessageDialog(null,"Erro ao Gravar" + ex.getMessage());
        }
        this.limparComponentes();
        this.loadTable();
    }//GEN-LAST:event_BtnSalvarActionPerformed

