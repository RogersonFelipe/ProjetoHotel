    private void BtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSalvarActionPerformed
        Pagamentos p = this.createObject();
        try {
            if(TCodigo.getText().isEmpty()) {
                dao.create(p);
                Quartos quarto = p.getReservas().getQuartos();
                quarto.setStatus(Status.A);
                new DaoQuartos().update(quarto);
            }else {
                dao.update(p);
                JOptionPane.showMessageDialog(null, "Pagamento Atualizado com sucesso!");
            }
            JOptionPane.showMessageDialog(null, "Pagamento salvo com sucesso!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao Gravar" + ex.getMessage());
        }
        this.loadCReservas(null);
        this.limparComponentes();
        this.loadTable();
    }//GEN-LAST:event_BtnSalvarActionPerformed

    private void BtnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRemoverActionPerformed
        if(TCodigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Selecione um dos Registros");
            return;
        }
        if(JOptionPane.showConfirmDialog(null, "Deseja Remover?") != 0){
            return;
        }
        try{
            int codigo = Integer.parseInt(TCodigo.getText());
            Pagamentos p = dao.read(Pagamentos.class, codigo);
            dao.delete(p);
            Quartos quarto = p.getReservas().getQuartos();
            quarto.setStatus(Status.R);
            new DaoQuartos().update(quarto);
            JOptionPane.showMessageDialog(null, "Pagamento excluído e quarto atualizado para 'Reservado'!");
            this.loadCReservas(null);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Erro" + ex.getMessage());
        }
        this.limparComponentes();
        this.loadTable();
    }//GEN-LAST:event_BtnRemoverActionPerformed

