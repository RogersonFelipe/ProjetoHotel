    //Carregar Reserva
    private void loadCReservas(){
        loadCReservas(null);
    }
    
    private void loadCReservas(Reservas reservaAtual){
        DaoReservas dao = new DaoReservas();
        List<Reservas> lista = dao.read();  
        lista.removeIf(r -> r.getQuartos()== null || r.getQuartos().getStatus() != enums.Status.R);
        if ((lista == null || lista.isEmpty()) && reservaAtual == null) {
            JOptionPane.showMessageDialog(this,
                    "Não há Pagamentos no momento.",
                    "Aviso",
                    JOptionPane.INFORMATION_MESSAGE);
        }
        if (reservaAtual != null && !lista.contains(reservaAtual)) {
            lista.add(reservaAtual);
        }
        DefaultComboBoxModel cbm = new DefaultComboBoxModel(lista.toArray());
        CReserva.setModel(cbm);

        if (reservaAtual != null) {
            CReserva.setSelectedItem(reservaAtual);
        }
        
    }
    
    //Carregar Enum
    private void loadCMetodo(){
        DefaultComboBoxModel cbm = new DefaultComboBoxModel(MetodoPagamento.values());
        CMetodo.setModel(cbm);
    }
    
    //Pegar Valor Total Reserva
    private void atualizarValorReserva() {
        Reservas reservaSelecionada = (Reservas) CReserva.getSelectedItem();
        if (reservaSelecionada != null) {
            BigDecimal valorTotal = reservaSelecionada.getTotal();
            if (valorTotal != null) {
                TValor.setText(valorTotal.setScale(2, java.math.RoundingMode.HALF_UP).toString());
            } else {
                TValor.setText("0.00");
            }
        } else {
            TValor.setText("0.00");
        }
    }
    
    //Zerar Componentes
    private void limparComponentes(){
        TCodigo.setText("");
        if(CReserva.getItemCount() > 0){
            CReserva.setSelectedIndex(0);
        }
        CMetodo.setSelectedIndex(0);
        FTDataPagamento.setText("");
        TADesc.setText("");
        TFiltro.setText("");
        CReserva.requestFocus();
    }
    
    //Montar Objeto
    private Pagamentos createObject(){
        Pagamentos p = new Pagamentos();
        if(TCodigo.getText().isEmpty()){
            p.setId(0);
        }else{
            p.setId(Integer.parseInt(TCodigo.getText()));
        }
        p.setReservas((Reservas)CReserva.getSelectedItem());
        p.setMetodo_pagamento((MetodoPagamento)CMetodo.getSelectedItem());
        p.setData_pagamento(LocalDate.parse(FTDataPagamento.getText(), dtf));
        p.setValor(new BigDecimal(TValor.getText().replace(",", ".")));
        p.setDescricao(TADesc.getText());
        
        return p;
    }
    
    //Popular Componentes
    private void loadComponents(Pagamentos p){
        TCodigo.setText(p.getId()+"");
        CReserva.setSelectedItem(p.getReservas());
        CMetodo.setSelectedItem(p.getMetodo_pagamento());
        TADesc.setText(p.getDescricao());
        FTDataPagamento.setText(p.getData_pagamento().format(dtf));
        TValor.setText(p.getValor().setScale(2, java.math.RoundingMode.HALF_UP).toString());
    }

