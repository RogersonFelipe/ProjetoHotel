    //Zerar componentes
    private void limparComponentes(){
        TCodigo.setText("");
        if (CReserva.getItemCount() > 0) {
            CReserva.setSelectedIndex(0);
        }
        CAvaliacao.setSelectedIndex(0);
        TAObserve.setText("");
    }
    
    //Montar Objeto
    private AvaliacaoQuarto createObject(){
        AvaliacaoQuarto a = new AvaliacaoQuarto();
        if(TCodigo.getText().isEmpty()){
            a.setId(0);
        }else{
            a.setId(Integer.parseInt(TCodigo.getText()));
        }
        a.setAvaliacao((Avaliacao) CAvaliacao.getSelectedItem());
        a.setReservas((Reservas) CReserva.getSelectedItem());
        a.setObservacao(TAObserve.getText());
        return a;
    }
    
    //Popular Componentes
    private void loadComponentes(AvaliacaoQuarto a){
        TCodigo.setText(a.getId()+"");
        CAvaliacao.setSelectedItem(a.getAvaliacao());
        CReserva.setSelectedItem(a.getReservas());
        TAObserve.setText(a.getObservacao());
    }
    
    //Carregar enum
    private void loadAvaliacao(){
        DefaultComboBoxModel cbm = new DefaultComboBoxModel(Avaliacao.values());
        CAvaliacao.setModel(cbm);
    }
    
    //Carregar Reserva
     private void loadCReserva() {
       loadCReserva(null);
    }
    private void loadCReserva(Reservas reservaAtual) {
        DaoReservas dao = new DaoReservas();
        List<Reservas> lista = dao.readComQuartosAlugados();

        if ((lista == null || lista.isEmpty()) && reservaAtual == null) {
            JOptionPane.showMessageDialog(this,
                    "Não há reservas disponíveis para avaliação no momento.",
                    "Aviso",
                    JOptionPane.INFORMATION_MESSAGE);
        }

        if (reservaAtual != null && !lista.contains(reservaAtual)) {
            lista.add(reservaAtual);
        }

        DefaultComboBoxModel cbm = new DefaultComboBoxModel(lista.toArray());
        CReserva.setModel(cbm);

        if (reservaAtual != null) {
            CReserva.setSelectedItem(reservaAtual);
        }
    }

