    private void BtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSalvarActionPerformed
        AvaliacaoQuarto a = this.createObject();
        try{
            if(TCodigo.getText().isEmpty()){
                dao.create(a);
                JOptionPane.showMessageDialog(null, "Avaliação salva com sucesso!");
            }else{
                dao.update(a);
                JOptionPane.showMessageDialog(null, "Avaliação atualizada com sucesso!");
            }
            if (a.getReservas() != null && a.getReservas().getQuartos() != null) {
                DaoQuartos daoQ = new DaoQuartos();
                var quarto = a.getReservas().getQuartos();
                quarto.setStatus(enums.Status.L);
                daoQ.update(quarto);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao gravar: " + ex.getMessage());
        }
        this.limparComponentes();
        this.loadTable();
        this.loadCReserva(null);
    }//GEN-LAST:event_BtnSalvarActionPerformed

    private void BtnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRemoverActionPerformed
        if(TCodigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Selecione um dos Registros:");
            return;
        }
        if(JOptionPane.showConfirmDialog(null,"Deseja Remover?") != 0){
            return;
        }
        try{
            int codigo = Integer.parseInt(TCodigo.getText());
            AvaliacaoQuarto a = dao.read(AvaliacaoQuarto.class, codigo);
            if (a.getReservas() != null && a.getReservas().getQuartos() != null) {
                DaoQuartos daoQ = new DaoQuartos();
                var quarto = a.getReservas().getQuartos();
                quarto.setStatus(enums.Status.A);
                daoQ.update(quarto);
            }
            dao.delete(a);
            JOptionPane.showMessageDialog(null, "Avaliação removida com sucesso!");
        }catch (Exception ex){
            JOptionPane.showMessageDialog(null,"Erro:" + ex.getMessage());
        }
        this.limparComponentes();
        this.loadTable();
        this.loadCReserva(null);
    }//GEN-LAST:event_BtnRemoverActionPerformed

