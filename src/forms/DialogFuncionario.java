    private void BtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSalvarActionPerformed
        Funcionarios f = this.createObject();
        try{
            if(TCodigo.getText().isEmpty()){
                dao.create(f);
            }else{
                dao.update(f);
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Erro ao Gravar" + ex.getMessage());
        }
        this.limparCompanentes();
        this.loadTable();
    }//GEN-LAST:event_BtnSalvarActionPerformed

    private void BtnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRemoverActionPerformed
        if(TCodigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Selecione um dos Registros:");
            return;
        }
        if(JOptionPane.showConfirmDialog(null,"Deseja Remover?")!= 0){
            return;
        }
        try{
            int codigo = Integer.parseInt(TCodigo.getText());
            Funcionarios f = dao.read(Funcionarios.class, codigo);
            dao.delete(f);
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null,"Erro:" + ex.getMessage());
        }
        this.limparCompanentes();
        this.loadTable();
    }//GEN-LAST:event_BtnRemoverActionPerformed

