    private void limparCompanentes(){
        TCodigo.setText("");
        TNome.setText("");
        CCargo.setSelectedIndex(0);
        FTSalario.setValue(0.00);
        TFiltro.setText("");
        TNome.requestFocus();
    }
    
    //Montar Objeto
    private Funcionarios createObject() {
        Funcionarios f = new Funcionarios();

        if (TCodigo.getText().isEmpty()) {
            // Novo Registro
            f.setId(0);
        } else {
            f.setId(Integer.parseInt(TCodigo.getText()));
        }

        f.setNome(TNome.getText());
        f.setCargos((Cargos) CCargo.getSelectedItem());
        f.setSalario(BigDecimal.valueOf(((Number) FTSalario.getValue()).doubleValue()));


        return f;
    }
    
    //Popular componente
    private void loadComponents(Funcionarios f){
        TCodigo.setText(f.getId()+"");
        TNome.setText(f.getNome());
        CCargo.setSelectedItem(f.getCargos());
        FTSalario.setValue(f.getSalario());
    }
    
    //Carregar Enum
    private void loadCargo(){
        DefaultComboBoxModel cbm = new DefaultComboBoxModel(Cargos.values());
        CCargo.setModel(cbm);
    }
    
